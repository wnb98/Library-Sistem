    private void EditBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EditBtnActionPerformed
        // TODO add your handling code here:
         if (!(StNameTF.getText().isEmpty() || StDepTF.getText().isEmpty() || StPhoneTF.getText().isEmpty() || StSemCom.getSelectedIndex()== -1)) {
            try {
                String UpdateQuery = "Update StudentTbl set StName=?, StDep=?, StPhone=?, StSem=? where StId=?";
                Con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/LibraryDb", "root", "weamalbattah");
                PreparedStatement Save = Con.prepareStatement(UpdateQuery);
                Save.setInt(5, Key);
                Save.setString(1, StNameTF.getText());
                Save.setString(2, StDepTF.getText());
                Save.setString(3, StPhoneTF.getText());
                Save.setInt(4, Integer.valueOf(StSemCom.getSelectedIndex())); 
                if (Save.executeUpdate() == 1) {
                    DisplayStud();
                    JOptionPane.showMessageDialog(this, "Student Updated!");
                } else {
                    JOptionPane.showMessageDialog(this, "Something went wrong!");

                }

            } catch (Exception e) {
                JOptionPane.showMessageDialog(this,e);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Missing Information!");

        }
    }//GEN-LAST:event_EditBtnActionPerformed

